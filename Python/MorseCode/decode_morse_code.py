symbols = {
    ".-": "a",
    "-...": "b",
    "-.-.": "c",
    "-..": "d",
    ".": "e",
    "..-.": "f",
    "--.": "g",
    "....": "h",
    "..": "i",
    ".---": "j",
    "-.-": "k",
    ".-..": "l",
    "--": "m",
    "-.": "n",
    "---": "o",
    ".--.": "p",
    "--.-": "q",
    ".-.": "r",
    "...": "s",
    "-": "t",
    "..-": "u",
    "...-": "v",
    ".--": "w",
    "-..-": "x",
    "-.--": "y",
    "--..": "z",
    "/": " "  # Handling spaces between words
}

morse_code = input("Enter Morse Code: ").strip()
morse_chars = morse_code.split(" ")
decoded_message = ""

for char in morse_chars:
    if char in symbols:
        decoded_message += symbols[char]

print("Decoded message: ", decoded_message)
